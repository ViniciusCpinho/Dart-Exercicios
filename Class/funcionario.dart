
class Funcionario {
  String? nome;
  int? idade;
  String? cpf;
  String? salario;
  String? cargo;

  Funcionario({String? nome, int? idade, String? cpf, String? salario, String? cargo}) {
    this.nome = nome;
    this.idade = idade;
    this.cpf = cpf;
    this.salario = salario;
    this.cargo = cargo;
  }
}



